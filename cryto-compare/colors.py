## ANSI Colors
BLACK  = '\33[30m'
RED    = '\33[31m'
GREEN  = '\33[32m'
YELLOW = '\33[33m'
BLUE   = '\33[34m'
VIOLET = '\33[35m'
BEIGE  = '\33[36m'
WHITE  = '\33[37m'
CYAN = '\033[96m'
ESCAPE = '\033[00m'


## Color functions
def print_red(prt): print(RED+prt+ESCAPE)
def print_blue(prt): print(BLUE+prt+ESCAPE)
def print_yellow(prt): print(YELLOW+prt+ESCAPE)
def print_green(prt): print(GREEN+prt+ESCAPE)
def print_violet(prt): print(VIOLET+prt+ESCAPE)
def print_cyan(prt): print(CYAN+prt+ESCAPE)



